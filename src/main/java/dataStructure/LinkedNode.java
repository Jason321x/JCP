package dataStructure;

/**
 * @className: LinkedNode
 * @description: 链表
 * @author: jxx
 * @date: 2021/1/23 6:21 PM
 **/
public class LinkedNode {
    public int val;
    public LinkedNode next;

    public int getVal() {
        return val;
    }

    public void setVal(int val) {
        this.val = val;
    }

    public LinkedNode getNext() {
        return next;
    }

    public void setNext(LinkedNode next) {
        this.next = next;
    }
}
